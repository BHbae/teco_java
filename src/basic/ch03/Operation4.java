package basic.ch03;

/**
 * 복합 대입 연산자란
 * 대입 연산자와 다른 연산자가 함꼐 쓰이는것(산술연산자)
 * 
 */
public class Operation4 {

	public static void main(String[] args) {
		
		int num1 = 0;
		// num1 += num1 + 2;
		num1 += 2;
		
		int num2 = 0;
		// num2 = num2 - 2;
		num2 -= 2;
		
		int num3 = 1;
		//num3 = num2 * 3;
		num3 *= 3;
		
		int num4 = 1;
		// num4 = num4 / 3;
		num4 /= 3;
				
				
		
		


	
		
	} //end of main

} // end of class
